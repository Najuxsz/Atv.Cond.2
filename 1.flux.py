#Verificar se x == 0
#Se verdadeiro, print "Zero"
#Se falso, verificar se x == 1
#Se verdadeiro, print "um"
#Se falso, verificar se x == 2
#Se verdadeiro, print "dois"
#Se falso, print "erro"

valorX = int(input("Informe o valor de x aqui: "))
if valorX == 0:
    print("Zero")
elif valorX == 1:
    print("Um")
elif valorX == 2:
    print("Dois")
else:
    print("Erro")
    

